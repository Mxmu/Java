public class IfElseIfDemo {
    public static void main(String[] args){
         int grade=75;
         if(grade>80) {
             System.out.println(grade + "成绩是优");
         }else if(grade>70) {
             System.out.println(grade + "成绩是良");
         }else if(grade>60) {
             System.out.println(grade + "成绩是中");
         }else {
             System.out.println(grade+"成绩是差");
         }
         }
    }


